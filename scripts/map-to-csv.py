def remove_keys_from_map(keys_to_remove, input_map):
    filtered_map = {key: value for key, value in input_map.items() if key not in keys_to_remove and not (isinstance(key, float) and math.isnan(key))}
    if any(math.isnan(key) if isinstance(key, float) else False for key in filtered_map.keys()):
        print("NaN detected in filtered_map!")
    return filtered_map

def create_map_from_csv(file_path):
    result_map = {}
    with open(file_path, mode='r', encoding='utf-8') as file:
        reader = csv.reader(file)
        next(reader)
        for row in reader:
            if len(row) >= 2 and row[0].strip():
                if row[0].lower() == 'nan' or (isinstance(row[0], float) and math.isnan(float(row[0]))):
                    print(f"NaN detected in CSV! Row: {row}")
                else:
                    result_map[row[0]] = row[1]
    return result_map


def create_csv_from_map(output_file_path, input_map):
    with open(output_file_path, mode='w', newline='', encoding='utf-8') as file:
        writer = csv.writer(file)
        writer.writerow(['CrimeDescription', 'Category'])  
        for crime, category in input_map.items():
            if isinstance(crime, float) and math.isnan(crime):
                print(f"NaN detected in input_map while writing! Key: {crime}, Value: {category}")
            if not (isinstance(crime, float) and math.isnan(crime)) and crime != 'nan' and str(crime).strip():
                writer.writerow([crime, category])

    print(f"Category map saved to {output_file_path}.")


